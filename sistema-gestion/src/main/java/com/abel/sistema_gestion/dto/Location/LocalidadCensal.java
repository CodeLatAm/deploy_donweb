package com.abel.sistema_gestion.dto.Location;

public class LocalidadCensal {
    private String id;
    private String nombre;

    public LocalidadCensal() {
    }

    // Getters y setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
