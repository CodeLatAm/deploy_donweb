package com.abel.sistema_gestion.dto.Location;

public class Centroide {
    private double lat;
    private double lon;

    public Centroide() {
    }

    // Getters y setters
    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
