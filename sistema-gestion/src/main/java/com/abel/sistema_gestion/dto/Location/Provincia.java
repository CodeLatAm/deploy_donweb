package com.abel.sistema_gestion.dto.Location;

public class Provincia {
    private String id;
    private String nombre;

    public Provincia() {
    }

    // Getters y setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
